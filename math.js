//Method

//round
console.log(Math.round(678.90));
console.log(Math.round(-234.876));
console.log(Math.round(0.456));

//floor
console.log(Math.floor(458.08));
console.log(Math.floor(-6540.87));
console.log(Math.floor(0.987));
console.log(Math.floor(67));

//ceil
console.log(Math.ceil(345.47));
console.log(Math.ceil(67));
console.log(Math.ceil(0));

//abs
console.log(Math.abs(-25));
console.log(Math.abs(87));
console.log(Math.abs(0));

//sqrt
console.log(Math.sqrt(225));
console.log(Math.sqrt(-64));
console.log(Math.sqrt(432));
console.log(Math.sqrt(34.89));

//power
console.log(Math.pow(45, 2));
console.log(Math.pow(45, 0.2));
console.log(Math.pow(45, -2));
console.log(Math.pow(45, 0));

//min max
console.log(Math.min(45, -78, 87, 200, -400, 234, 0.87));
console.log(Math.max(45, -78, 87, 200, -400, 234, 0.87));

//random
console.log(Math.random());
console.log(Math.random()*2);
console.log(Math.floor(Math.random()*10));
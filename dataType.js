// data type tells the type of data that a variable can hold. In JavaScript, there are several data types, including:

myvar = 25; // number
console.log(myvar); // Output: 25

myvar = "Hello, World!"; // string
console.log(myvar); // Output: Hello, World!

myvar = true; // boolean
console.log(myvar); // Output: true

console.log("5" - 3); // Output: 2 (string is converted to number)  
console.log("5" + 3); // Output: "53" (number is converted to string)